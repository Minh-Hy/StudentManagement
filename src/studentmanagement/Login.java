    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(isEmptyStudent()){
            if(!student.isEmailExist(jTextField4.getText())){
                if(!student.isPhoneNumberExist(jTextField5.getText())){
                    int id = student.getMax();
                    String name = jTextField2.getText();
                    SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                    String date = dateFormat.format(jDateChooser1.getDate());
                    String gender = jComboBox1.getSelectedItem().toString();
                    String email = jTextField4.getText();
                    String phone = jTextField5.getText();
                    String father = jTextField6.getText();
                    String mother = jTextField7.getText();
                    String address = jTextField8.getText();
                    student.insert(id, name, date, gender, email, phone, father, mother, address);
                    jTable1.setModel(new DefaultTableModel(null, new Object[]{"Student's ID", "Student's Name", "Date Of Birth", "Gender", "Email",
                        "Phone Number", "Father's Name", "Mother's Name", "Address"}));
                    student.getStudentValue(jTable1, "");
                    clearStudent();
                }else {
                    JOptionPane.showMessageDialog(this, "This phone number is already exists");
                }
            } else {
                JOptionPane.showMessageDialog(this, "This email is already exists");
            }
            

        }
    }//GEN-LAST:event_jButton3ActionPerformed

